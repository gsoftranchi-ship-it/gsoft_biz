abstract class BaseRepository<T> {
  ///===========================================================
  /// Create
  ///===========================================================
  Future<void> create(
      String gymId,
      T model,
      );

  ///===========================================================
  /// Update
  ///===========================================================
  Future<void> update(
      String gymId,
      T model,
      );

  ///===========================================================
  /// Get By Id
  ///===========================================================
  Future<T?> get(
      String gymId,
      String id,
      );

  ///===========================================================
  /// Get All
  ///===========================================================
  Future<List<T>> getAll(
      String gymId,
      );

  ///===========================================================
  /// Soft Delete
  ///===========================================================
  Future<void> softDelete(
      String gymId,
      String id,
      );
}